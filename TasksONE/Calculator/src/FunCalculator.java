public class FunCalculator {
    double valorUM;
    double valorDOIS;

    double somar(double valorUM, double valorDOIS){
        return valorUM + valorDOIS;
    }

    double subtrair(double valorUM, double valorDOIS){
        return valorUM - valorDOIS;
    }

    double multi(double valorUM, double valorDOIS){
        return valorUM * valorDOIS;
    }

    double division(double valorUM, double valorDOIS){
        return valorUM / valorDOIS;
    }
}
